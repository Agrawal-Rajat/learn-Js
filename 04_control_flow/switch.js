const month = 4;
switch(month){
    case 1 : 
    console.log("jan");
    break;
    case 2 : 
    console.log("feb");
    break;
    case 3 : 
    console.log("march");
    break;
    case 4 : 
    console.log("april");
    break;
    case 5 : 
    console.log("may");
    break;

    default : 
    console.log("none");
}

// agar break hata denge toh uska baad ke sare case print honge except default